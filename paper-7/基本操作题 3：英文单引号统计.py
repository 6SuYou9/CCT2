'''
从键盘输入一个字符串,检查并统计字符串中包含的英文单引号的对数。如果没有找到单引号,就在屏
幕上显示"没有单引号";每统计到2个单引号,就算一对,女0果找到2对单引号,就显示"有2对单引
号";如果找到3个单引号,就显示"有1对配对单引号,存在没有配对的单引号"。
示例1:
输入:"dfd'dfa'fd'"
输出:"有1对配对单引号,存在没有配对的单引号"
'''

#在...处填写多行代码
#不允许修改其他代码

st = input()
pair = 0
for s in st:
    if s == "'":
        pair += 1
if pair == 0:
    pro = "没有单引号"
elif pair % 2 == 0:
    pro = "有{}对单引号".format(int(pair//2))
else:
    pro = "有{}对配对单引号，存在没有配对的单引号".format(int(pair//2))
print(pro)
